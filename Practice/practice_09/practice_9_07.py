# Задача 7: Результаты экзамена
"""
Напишите метод exam_result(score), который принимает балл (от 0 до 100).
Метод должен вернуть:
- "Неудовлетворительно", если балл меньше 50.
- "Удовлетворительно", если от 50 до 69.
- "Хорошо", если от 70 до 89.
- "Отлично", если 90 или выше.
"""
# Назначаем функцию
def exam_result(score):
    # Определяем результат экзамена на основе балла
    if score < 50:
        return "Неудовлетворительно"
    elif 50 <= score <= 69:
        return "Удовлетворительно"
    elif 70 <= score <= 89:
        return "Хорошо"
    else:
        return "Отлично"

# Вызов и вывод
print(exam_result(45))  # Неудовлетворительно
print(exam_result(60))  # Удовлетворительно
print(exam_result(75))  # Хорошо
print(exam_result(95))  # Отлично
