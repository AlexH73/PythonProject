# Задание 6: Числа в разных системах счисления
"""
Создайте переменную:
- value = 255
Выведите число в:
1. Десятичной системе: "255"
2. Двоичной системе: "11111111"
3. Шестнадцатеричной системе: "ff"
"""
# Создаем переменную
value = 255

# Выводим число в различных системах счисления
decimal_value = value
binary_value = bin(value)[2:]
hexadecimal_value = hex(value)[2:]

# Выводим результат
print(f"Десятичная система: {decimal_value}")
print(f"Двоичная система: {binary_value}")
print(f"Шестнадцатеричная система: {hexadecimal_value}")
