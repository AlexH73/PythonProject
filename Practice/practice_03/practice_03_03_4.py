# Задание 4: Выравнивание текста с f-строками
"""
Создайте переменные:
- column1 = "Товар"
- column2 = "Цена"
- column3 = "Количество"
- product = "Хлеб"
- price = 3.2
- quantity = 2
Выведите данные, выровненные:
- Названия колонок по центру на 15 символов.
- Значения данных выровнены вправо на 15 символов.
"""
# Создаем переменные
column1 = "Товар"
column2 = "Цена"
column3 = "Количество"
product = "Хлеб"
price = 3.2
quantity = 2

# Выводим данные, выровненные по центру и вправо
print(f"{column1:^15} {column2:^15} {column3:^15}")
print(f"{product:>15} {price:>15.2f} {quantity:>15}")
