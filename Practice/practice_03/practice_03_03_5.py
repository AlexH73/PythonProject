# Задание 5: Форматирование числа с округлением
"""
Создайте переменную:
- число = 3.1415926535
Сформатируйте строку:
"Число Пи округлено до трёх знаков: 3.142"
"""
# Этот код использует константу Pi из модуля math и выводит её значение, округленное до трех знаков после запятой.
import math

# Используем константу Pi
Pi = math.pi

# Форматируем строку
formatted_string = f"Число Пи округлено до трёх знаков: {Pi:.3f}"

# Выводим результат
print(formatted_string)
