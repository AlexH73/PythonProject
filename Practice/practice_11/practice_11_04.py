# Задача 4: Угадать число
"""
Напишите программу, которая загадывает число от 1 до 10.
Пользователь пытается угадать его, вводя числа.
Программа должна сообщать, угадано ли число. Цикл продолжается, пока пользователь не угадает число.
"""
import random

def guess_the_number():
    # Генерация случайного числа от 1 до 10
    secret_number = random.randint(1, 10)

    print("Я загадал число от 1 до 10. Попробуйте угадать!")

    # Инициализация переменной для хранения ответа пользователя
    user_guess = None

    # Цикл продолжается, пока пользователь не угадает число
    while user_guess != secret_number:
        user_guess = int(input("Введите ваше предположение: "))

        if user_guess < secret_number:
            print("Загаданное число больше.")
        elif user_guess > secret_number:
            print("Загаданное число меньше.")
        else:
            print("Поздравляю! Вы угадали!")


# Вызов функции
guess_the_number()