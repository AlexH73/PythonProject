# Задача 1: Счётчик чисел
"""
Напишите программу, которая с помощью цикла while выводит числа от 1 до 10.

# Инициализация счётчика
number = 1

# Цикл while для вывода чисел от 1 до 10
while number <= 10:
    print(number)
    number += 1  # Увеличиваем счётчик на 1
"""
def print_numbers():
    # Инициализация счётчика
    number = 1
    result = ""

    # Цикл while для вывода чисел от 1 до 10
    while number <= 10:
        result += str(number) + " "  # Добавляем число к строке с пробелом
        number += 1  # Увеличиваем счётчик на 1

    print(result.strip())  # Выводим строку без лишнего пробела в конце

# Вызов функции
print_numbers()