# Задача 7: Счётчик гласных в слове
"""
Напишите программу, которая принимает от пользователя строку и подсчитывает количество
гласных букв (а, е, ё, и, о, у, ы, э, ю, я) в этой строке с помощью цикла while.
"""
def count_vowels():
    # Запрашиваем строку от пользователя
    user_input = input("Введите строку: ").lower()  # Приводим строку к нижнему регистру для удобства проверки

    # Определяем гласные буквы
    vowels = "аеёиоуыэюяeiuoüäaö"

    # Инициализация переменных
    index = 0
    vowel_count = 0

    # Цикл while для перебора символов строки
    while index < len(user_input):
        if user_input[index] in vowels:  # Проверяем, является ли символ гласным
            vowel_count += 1
        index += 1  # Переходим к следующему символу

    # Вывод результата
    print(f"Количество гласных букв в строке: {vowel_count}")


# Вызов функции
count_vowels()
