# Задача 2: Сумма чисел от 1 до N
"""
Напишите программу, которая принимает число N от пользователя 
и с помощью цикла while вычисляет сумму всех чисел от 1 до N.
"""
def sum_numbers_up_to_n():
    # Запрос числа у пользователя
    n = int(input("Введите число N: "))

    # Инициализация переменных
    current = 1
    total_sum = 0

    # Цикл while для вычисления суммы
    while current <= n:
        total_sum += current  # Добавляем текущее число к сумме
        current += 1  # Переходим к следующему числу

    # Вывод результата
    print(f"Сумма чисел от 1 до {n} равна {total_sum}")

# Вызов функции
sum_numbers_up_to_n()
