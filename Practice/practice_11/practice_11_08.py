# Задача 8: Минимальный делитель числа
"""
Напишите программу, которая принимает от пользователя число N (больше 1)
и находит его минимальный делитель, кроме 1. Используйте цикл while.
"""
def find_min_divisor(n):
  """
  Находит минимальный делитель числа n (больше 1), кроме 1.

  Args:
      n: Целое число больше 1.

  Returns:
      Минимальный делитель числа n или None, если n меньше или равно 1.
  """
  if n <= 1:
    return None  # Возвращаем None, если n не удовлетворяет условию

  divisor = 2  # Начинаем проверку с 2, так как 1 не считается
  while divisor <= n:
      if n % divisor == 0:
          return divisor # Возвращаем минимальный делитель, как только он найден
      divisor += 1 # Увеличиваем делитель и продолжаем поиск


# Получаем ввод от пользователя
while True:
  try:
    n = int(input("Введите целое число N (больше 1): "))
    if n > 1:
        break  # Выходим из цикла, если ввод корректный
    else:
        print("Число должно быть больше 1. Попробуйте еще раз.")
  except ValueError:
      print("Некорректный формат ввода. Пожалуйста, введите целое число.")

# Находим и выводим результат
min_divisor = find_min_divisor(n)

if min_divisor is not None:
    print(f"Минимальный делитель числа {n} (кроме 1): {min_divisor}")
else:
    print("Не удалось найти минимальный делитель, так как было введено некорректное число.")