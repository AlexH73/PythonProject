# Задача 5: Проверка ввода
"""
Напишите программу, которая запрашивает у пользователя ввод числа больше 10.
Если пользователь вводит число меньше или равное 10, программа должна снова запрашивать ввод.
Цикл завершается, когда введено число больше 10.
"""
def input_number():
    while True:
        # Запрашиваем ввод у пользователя
        number = int(input("Введите число больше 10: "))

        # Проверяем, удовлетворяет ли условие
        if number > 10:
            print(f"Спасибо! Вы ввели число {number}.")
            break  # Завершаем цикл
        else:
            print("Число должно быть больше 10. Попробуйте снова.")


# Вызов функции
input_number()