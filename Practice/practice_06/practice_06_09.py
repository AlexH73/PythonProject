# Задание 9: Преобразование числа
"""
Создайте метод, который принимает вещественное число.
Метод должен вернуть это число, округлённое до двух знаков после запятой.
"""
def round_to_two_decimal_places(number):
    """
    Округляет вещественное число до двух знаков после запятой.

    Аргументы:
    number (float): Вещественное число.

    Возвращает:
    float: Число, округлённое до двух знаков после запятой.
    """
    return round(number, 2)

# Пример использования метода
rounded_number = round_to_two_decimal_places(3.14159)
print(f"Округлённое число: {rounded_number}")
