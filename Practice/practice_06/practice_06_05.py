# Задание 5: Итоговая стоимость покупки
"""
Создайте метод, который принимает цену товара и количество товаров.
Метод должен рассчитать и вернуть итоговую стоимость покупки.
"""
def calculate_total_cost(price, quantity):
    """
    Рассчитывает итоговую стоимость покупки.

    Аргументы:
    price (float): Цена товара.
    quantity (int): Количество товаров.

    Возвращает:
    float: Итоговая стоимость покупки.
    """
    return price * quantity

# Пример использования метода
total_cost = calculate_total_cost(100.0, 3)
print(f"Итоговая стоимость покупки: ${total_cost}")
