# Задание 1: Приветствие пользователя
"""
Создайте метод, который принимает имя пользователя в качестве аргумента.
Метод должен вывести строку вида: "Привет, [Имя]!"
"""
# Метод с аргументами.
def greet_user(name):
    # Выводим приветственное сообщение с именем пользователя
    print(f"Привет, {name}!")

# Пример использования метода
greet_user("Александр")
