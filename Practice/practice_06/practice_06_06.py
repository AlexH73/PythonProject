# Задание 6: Длина строки
"""
Создайте метод, который принимает строку.
Метод должен вернуть длину этой строки.
"""
def string_length(s):
    """
    Возвращает длину строки.

    Аргументы:
    s (str): Входная строка.

    Возвращает:
    int: Длина строки.
    """
    return len(s)

# Пример использования метода
length = string_length("Привет, мир!")
print(f"Длина строки: {length}")
