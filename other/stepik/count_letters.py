"""
Создайте функцию count_letters, которая принимает на вход фразу и подсчитывает, какое количество в ней строчных(K) и заглавных (N) букв, все остальные символы игнорируются. Функция должна вывести на экран информацию о найденных буквах в определенном формате.

Количество заглавных символов: N
Количество строчных символов: K
"""


def count_letters(phrase):
    # Инициализация счетчиков для строчных и заглавных букв
    lowercase_count = 0
    uppercase_count = 0

    # Проход по каждому символу в фразе
    for char in phrase:
        # Проверка, является ли символ строчной буквой
        if char.islower():
            lowercase_count += 1
        # Проверка, является ли символ заглавной буквой
        elif char.isupper():
            uppercase_count += 1

    # Вывод количества заглавных букв
    print("Количество заглавных символов:", uppercase_count)
    # Вывод количества строчных букв
    print("Количество строчных символов:", lowercase_count)


# Пример использования
phrase = input("Введите фразу: ")
count_letters(phrase)
