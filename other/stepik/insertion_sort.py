"""
Программа получает на вход число n - количество элементов в списке, и затем в следующей строке сам список.

Ваша задача отсортировать список по возрастанию при помощи сортировки вставками, в случае если элементы соседние совпадают менять их ненужно.
"""
def insertion_sort(numbers):
    """
    Сортирует список целых чисел по возрастанию с помощью сортировки вставками.

    Args:
        numbers: Список целых чисел.

    Returns:
        Отсортированный список целых чисел.
    """
    for i in range(1, len(numbers)): # Начинаем со второго элемента
        key = numbers[i] # Берем текущий элемент для вставки
        j = i - 1 # Индекс предыдущего элемента

        # Сдвигаем элементы, пока не найдем нужное место для вставки
        while j >= 0 and numbers[j] > key: # Проверяем, что индекс не меньше 0 и текущий элемент меньше элемента j
          numbers[j+1] = numbers[j] # Сдвигаем элемент
          j -= 1 # Двигаемся дальше
        numbers[j + 1] = key # Вставляем текущий элемент на нужное место
    return numbers # Возвращаем отсортированный список

# Получаем ввод от пользователя:
try:
    n = int(input("Введите количество элементов: "))
    if n < 0:
        print("Ошибка: Количество элементов не может быть отрицательным.")
    else:
        input_str = input("Введите элементы списка, разделенные пробелами: ")
        numbers = list(map(int, input_str.split()))

        if len(numbers) != n:
           print("Ошибка: Количество введенных чисел не соответствует заданному.")
        else:
            sorted_numbers = insertion_sort(numbers)
            print(*sorted_numbers)
except ValueError:
     print("Ошибка: Некорректный ввод. Пожалуйста, введите целое число и список целых чисел.")