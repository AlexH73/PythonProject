def print_pascal_triangle(n):
    """
    Выводит треугольник Паскаля заданного размера n.

    Args:
        n (int): Количество строк в треугольнике Паскаля (начиная с 0).
    """
    # 1. Инициализация треугольника
    #    Создаем список списков (матрицу) для хранения значений треугольника Паскаля.
    #    Размер матрицы (n+1) x (n+1).
    #    Первый элемент каждой строки инициализируется 1, а остальные нулями.
    triangle = [] #  пустой список для хранения строк треугольника
    for i in range(n + 1): # Перебираем строки
        triangle.append([1] + [0] * n) # Добавляем строку в список, первый элемент 1, остальные 0

    # 2. Вычисление значений треугольника Паскаля
    #    Заполняем матрицу значениями, используя правило треугольника Паскаля:
    #    каждый элемент равен сумме двух элементов из предыдущей строки.
    for i in range(1, n + 1): # Перебираем строки, начиная со второй
        for j in range(1, i + 1): # Перебираем столбцы в каждой строке
            triangle[i][j] = triangle[i - 1][j] + triangle[i - 1][j - 1] # Вычисляем значение элемента и сохраняем его в матрицу

    # 3. Вывод треугольника Паскаля
    #    Выводим на экран значения треугольника Паскаля,
    #    форматируя вывод: значения в каждой строке разделены пробелами.
    for i in range(0, n + 1): # Перебираем строки
        for j in range(0, i + 1): # Перебираем столбцы в строке
            print(triangle[i][j], end=" ") # Выводим элемент и пробел
        print() # Переход на новую строку

# Пример использования функции
n = int(input("Введите целое число: "))
print_pascal_triangle(n)