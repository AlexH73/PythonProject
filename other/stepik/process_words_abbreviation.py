def process_words():
    """
    Считывает слова и заменяет слишком длинные на аббревиатуры.
    """
    n = int(input("Введите количество слов: ")) # Считываем количество слов

    for _ in range(n): # Перебираем все слова
      word = input("Введите слово: ") # Считываем слово
      if len(word) > 10: # Проверяем длину слова
          abbreviation = word[0] + str(len(word) - 2) + word[-1]  # Создаем аббревиатуру
          print(abbreviation) # Выводим аббревиатуру
      else:
          print(word) # Выводим слово без изменений

# Вызываем функцию для обработки слов:
process_words()