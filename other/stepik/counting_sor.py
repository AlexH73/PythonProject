# Сортировка подсчетом
#
# Как видно из названия задачи, вам необходимо отсортировать список, состоящий только из чисел в пределах от -100 до 100 включительно,
# сортировкой подсчетом.
# Программа получает на вход число n - количество элементов в списке, затем сами элементы списка
# Вам необходимо вывести отсортированный список
#
# P.S. не пользуйтесь встроенной функцией sorted или методом sort

def counting_sort(numbers):
    """
    Сортирует список целых чисел в диапазоне [-100, 100] с помощью сортировки подсчетом.

    Args:
        numbers: Список целых чисел в диапазоне [-100, 100].

    Returns:
        Отсортированный список целых чисел.
    """
    min_val = -100  # Минимальное значение в диапазоне
    max_val = 100  # Максимальное значение в диапазоне
    count = [0] * (max_val - min_val + 1)  # Создаем список для подсчета элементов

    # Подсчитываем количество вхождений каждого числа
    for num in numbers:
      count[num - min_val] += 1

    sorted_numbers = [] # Создаем список для результата
    # Формируем отсортированный список
    for i in range(len(count)):
      for j in range(count[i]):
        sorted_numbers.append(i + min_val)
    return sorted_numbers  # Возвращаем отсортированный список

# Получаем ввод от пользователя
try:
    n = int(input("Введите количество элементов списка: "))
    if n < 0:
        print("Ошибка: Количество элементов не может быть отрицательным.")
    else:
        input_str = input("Введите элементы списка через пробел: ")
        numbers = list(map(int, input_str.split()))

        if len(numbers) != n:
            print("Ошибка: Количество введенных чисел не соответствует заданному.")
        else:
            sorted_numbers = counting_sort(numbers)
            print(sorted_numbers)
            #print(*sorted_numbers)
except ValueError:
    print("Ошибка: Некорректный ввод. Пожалуйста, введите целое число и список целых чисел.")