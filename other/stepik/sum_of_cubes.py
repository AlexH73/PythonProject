def sum_of_cubes():
    """
    Находит сумму кубов натуральных чисел от 50 до 100 включительно.

    Returns:
      Сумма кубов натуральных чисел от 50 до 100.
    """
    total_sum = 0  # Инициализируем сумму
    for number in range(50, 101): # Перебираем числа от 50 до 100
      cube = number ** 3  # Вычисляем куб числа
      total_sum += cube # Добавляем куб числа к сумме
    return total_sum # Возвращаем сумму

# Вызываем функцию и выводим результат
result = sum_of_cubes()
print(f"Сумма кубов чисел от 50 до 100: {result}")