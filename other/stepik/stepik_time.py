#Разность времен
"""
Даны значения двух моментов времени, принадлежащих одним и тем же суткам: часы, минуты и секунды для каждого из моментов времени.
Известно, что второй момент времени наступил не раньше первого. Определите, сколько секунд прошло между двумя моментами времени.

Входные данные:
Программа на вход получает три целых числа: часы, минуты, секунды, задающие первый момент времени и три целых числа, задающих второй момент времени.

Выходные данные:
Выведите число секунд между этими моментами времени.
"""

def time_to_seconds(hours, minutes, seconds):
    return hours * 3600 + minutes * 60 + seconds

# Ввод данных для первого момента времени
h1 = int(input("Введите часы для первого момента времени: "))
m1 = int(input("Введите минуты для первого момента времени: "))
s1 = int(input("Введите секунды для первого момента времени: "))

# Ввод данных для второго момента времени
h2 = int(input("Введите часы для второго момента времени: "))
m2 = int(input("Введите минуты для второго момента времени: "))
s2 = int(input("Введите секунды для второго момента времени: "))

# Преобразование моментов времени в секунды
time1 = time_to_seconds(h1, m1, s1)
time2 = time_to_seconds(h2, m2, s2)

# Вычисление разницы в секундах
difference = time2 - time1

print("Количество секунд между двумя моментами времени:", difference)
